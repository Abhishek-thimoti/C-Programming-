#include<stdio.h>
int main()
{
	int N;
	scanf("%d",&N);
	if(N==0)
	{
		printf("Zero");
	}
	else if(N==1)
	{
		printf("One");
	}
	else if(N==2)
	{
		printf("Two");
	}
	else if(N==3)
	{
		printf("Three");
	}
	else if(N==4)
	{
		printf("Four");
	}
	else if(N==5)
	{
		printf("Five");
	}
	else if(N==6)
	{
		printf("Six");
	}
	else if(N==7)
	{
		printf("Seven");
	}
	else if(N==8)
	{
		printf("Eight");
	}
	else if(N==9)
	{
		printf("Nine");
	}
	else
	{
		printf("Enter a Valid digit");
	}
	return 0;
}
